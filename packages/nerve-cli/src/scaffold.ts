const REPRODUCE_YML = `# Generated by \`nerve setup\` — edit freely, setup never overwrites.
# Byte-reproducibility: the committed dist/harness.json must rebuild
# identically from source. Deterministic compilation makes this checkable;
# \`nerve diff\` exits 1 on any semantic difference.
name: Nerve reproduce
on:
  push:
  pull_request:
jobs:
  reproduce:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: 22
      - run: npm ci || npm install
      - run: |
          if [ -f dist/harness.json ]; then
            npx nerve compile --out /tmp/fresh
            npx nerve diff dist/harness.json /tmp/fresh/harness.json
          else
            echo "No committed dist/harness.json — nothing to reproduce."
          fi
`

