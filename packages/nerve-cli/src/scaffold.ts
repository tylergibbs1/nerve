/**
 * Project scaffolding: `nerve init` (a project that can actually resolve
 * its own imports — package.json/tsconfig/.gitignore, not just two files)
 * and `nerve setup` (CI workflows: validate, snapshot, and the
 * byte-reproducibility check nobody else can offer).
 *
 * Every file is per-file refuse-to-overwrite: existing files are skipped
 * with a notice, never clobbered.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"
import type { Io } from "./index.js"

/** This CLI's own version — scaffolded projects pin the same line. */
export const cliVersion = (): string => {
  try {
    // package.json sits one level above both src/ (dev) and dist/ (published).
    const pkgPath = join(dirname(fileURLToPath(import.meta.url)), "..", "package.json")
    const pkg = JSON.parse(readFileSync(pkgPath, "utf8")) as { version?: string }
    return pkg.version ?? "latest"
  } catch {
    return "latest"
  }
}

